var b = 6;
var c = 2, d = 8;
var soma = a + b;
var sub = a - b;
var div = a / b;
var multi = a * b;
var pot = a**b;
var rest = a % b;
c = c + 1;
d = d - 1;

console.log(a, b, c, d, soma, sub, div);
